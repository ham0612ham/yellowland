package com.sp.app.bringcate;

public class Bringcate {
	private String cateJobNum;
	private String cateJobName;
	
	private String jobName;
	private String jobNum;
	
	private String siguNum;
	private String siguName;
	
	private String dongNum;
	private String dongName;
	
	public String getCateJobNum() {
		return cateJobNum;
	}
	public void setCateJobNum(String cateJobNum) {
		this.cateJobNum = cateJobNum;
	}
	public String getCateJobName() {
		return cateJobName;
	}
	public void setCateJobName(String cateJobName) {
		this.cateJobName = cateJobName;
	}
	public String getJobName() {
		return jobName;
	}
	public void setJobName(String jobName) {
		this.jobName = jobName;
	}
	public String getJobNum() {
		return jobNum;
	}
	public void setJobNum(String jobNum) {
		this.jobNum = jobNum;
	}
	public String getSiguNum() {
		return siguNum;
	}
	public void setSiguNum(String siguNum) {
		this.siguNum = siguNum;
	}
	public String getSiguName() {
		return siguName;
	}
	public void setSiguName(String siguName) {
		this.siguName = siguName;
	}
	public String getDongNum() {
		return dongNum;
	}
	public void setDongNum(String dongNum) {
		this.dongNum = dongNum;
	}
	public String getDongName() {
		return dongName;
	}
	public void setDongName(String dongName) {
		this.dongName = dongName;
	}
}
